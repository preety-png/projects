DROP TABLE IF EXISTS SONGS;
DROP TABLE IF EXISTS USER_DETAILS;
DROP TABLE IF EXISTS PLAYLIST;
DROP TABLE IF EXISTS TAGS;
DROP TABLE IF EXISTS TAGGING_AND_ADDSONG;
  
CREATE TABLE SONGS (
  song_id INT AUTO_INCREMENT  PRIMARY KEY,
  songs_name VARCHAR(250) NOT NULL,
  songs_artist VARCHAR(250) NOT NULL,
  songs_language VARCHAR(250) NOT NULL
);
CREATE TABLE USER_DETAILS (
  user_id INT AUTO_INCREMENT  PRIMARY KEY,
  user_name VARCHAR(250) NOT NULL,
  email VARCHAR(250) NOT NULL
);
CREATE TABLE PLAYLIST (
  playlist_id INT AUTO_INCREMENT  PRIMARY KEY,
  playlist_name VARCHAR(250) NOT NULL,
  user_id INT NOT NULL);

CREATE TABLE TAGS (
  tag_id INT AUTO_INCREMENT  PRIMARY KEY,
  tag_name VARCHAR(250) NOT NULL
);

CREATE TABLE TAGGING_AND_ADDSONG (
  playlistmapping_id INT AUTO_INCREMENT  PRIMARY KEY,
  playlist_id INT NOT NULL,
  song_id INT NOT NULL,
  tag_id INT
);

